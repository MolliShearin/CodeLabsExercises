//Example
let double = n => n * 2;

alert(double(5)); //10

//Function that accepts two arguments and returns their product
let product = (num1, num2) => num1 * num2
console.log(product(3, 4)); //12

//Fuction that takes in a string and returns it in uppercase
let upperString = string => string.toUpperCase()
console.log(upperString("I love cats"));

//Function with a single parameter, calculating the square of the provided number
let numberSquared = (number) => number * number
console.log(numberSquared(3));